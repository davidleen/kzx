/**
 * 战略目标责任人
 * @title com.education.book.bean
 * @date 2014-1-7
 * @author tanke
 */
public class TargetDutyersBean {
	
	private String id;
    private String executeCount;
	private String icon;
	private String sponsorCount;
	private String name;
	private String memberId;
	private String dangerCount;
	
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getExecuteCount() {
		return executeCount;
	}
	public void setExecuteCount(String executeCount) {
		this.executeCount = executeCount;
	}
	public String getIcon() {
		return icon;
	}
	public void setIcon(String icon) {
		this.icon = icon;
	}
	public String getSponsorCount() {
		return sponsorCount;
	}
	public void setSponsorCount(String sponsorCount) {
		this.sponsorCount = sponsorCount;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getMemberId() {
		return memberId;
	}
	public void setMemberId(String memberId) {
		this.memberId = memberId;
	}
	public String getDangerCount() {
		return dangerCount;
	}
	public void setDangerCount(String dangerCount) {
		this.dangerCount = dangerCount;
	}
	
}
